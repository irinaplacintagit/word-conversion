package com.challenge.model;

/**
 * @author iplacinta
 *
 */
public class IntNumberFormat
{
    private int number;
    private int div;
    private int mod;
    private int unit;
    
    public int getDiv()
    {
        return div;
    }
    public void setDiv(int div)
    {
        this.div = div;
    }
    public int getMod()
    {
        return mod;
    }
    public void setMod(int mod)
    {
        this.mod = mod;
    }
    public int getNumber()
    {
        return number;
    }
    public void setNumber(int number)
    {
        this.number = number;
    }
    public int getUnit()
    {
        return unit;
    }
    public void setUnit(int unit)
    {
        this.unit = unit;
    }
    
}
